package jACBr;

public class Aliquota 
{
    private String indice;
    private double aliquota;
    private char tipo;
    private double total;
    private int sequencia;
    
    public String getIndice()
    {
    	return indice;
    }
    
    public double getAliquota()
    {
    	return aliquota;
    }
    
    public char getTipo()
    {
    	return tipo;
    }
    
    public double getTotal()
    {
    	return total;
    }
    
    public int getSequencia()
    {
    	return sequencia;
    }
}
